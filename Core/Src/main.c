/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_TIM16_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_USART1_UART_Init();
  MX_RTC_Init();
  MX_TIM17_Init();
  MX_TIM14_Init();
  /* USER CODE BEGIN 2 */
  lcd_wr_stct.bkg_color = ST7735_BLACK;
  lcd_wr_stct.fore_color = ST7735_WHITE;

  LcdInit();

  htim1.Instance->CCR1 = 0;

  HAL_Delay(2000);
  print_set_speed();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  if (mcu_stop_flag)
	  {
		  mcu_stop_flag = false;
		  SystemClock_Config();
		  HAL_ResumeTick();
		  MX_RTC_Init();
	  }

	  if (!HAL_GPIO_ReadPin(SPEED_PLUS_GPIO_Port, SPEED_PLUS_Pin) ||
		  !HAL_GPIO_ReadPin(SPEED_MINUS_GPIO_Port, SPEED_MINUS_Pin))
	  {
		  HAL_Delay(DEBOUNCE_TIME);

		  if (run_flag)
		  {
			  MX_RTC_Init();
			  while (!HAL_GPIO_ReadPin(SPEED_PLUS_GPIO_Port, SPEED_PLUS_Pin) ||
					 !HAL_GPIO_ReadPin(SPEED_MINUS_GPIO_Port, SPEED_MINUS_Pin))
			  {
				  HAL_RTC_GetTime(&hrtc, &stimestructureget, RTC_FORMAT_BIN);
				  HAL_RTC_GetDate(&hrtc, &sdatestructureget, RTC_FORMAT_BIN);

				  if (stimestructureget.Seconds >= 2)
				  {
					  run_status_deinit();
					  break;
				  }
			  }
		  }

		  else
		  {
			  if (htim16.Instance->CCR1 == ZERO_BRIGHTNESS)
				  print_set_speed();
			  LcdSetBrightness(HALF_BRIGHTNESS);
			  while (!HAL_GPIO_ReadPin(SPEED_PLUS_GPIO_Port, SPEED_PLUS_Pin))
			  {
				  if (time_set < 5)
				  {
					  time_set += 1;
					  print_set_speed();
				  }
			  }

			  while (!HAL_GPIO_ReadPin(SPEED_MINUS_GPIO_Port, SPEED_MINUS_Pin))
			  {
				  if (time_set > 0)
				  {
					  time_set -= 1;
					  print_set_speed();
				  }
			  }
		  }
	  }

	  if (!HAL_GPIO_ReadPin(ON_GPIO_Port, ON_Pin) && !run_flag)
	  {
		  LcdClearScreen();
		  HAL_Delay(DEBOUNCE_TIME);

		  run_flag = true;
          LcdSetBrightness(LOW_BRIGHTNESS);
          tim14_counter = secs[time_set];
          HAL_Delay(REGISTER_WR_TIME);

          HAL_TIM_Base_Start_IT(&htim14);
          HAL_TIM_Base_Start_IT(&htim17);
          HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

	  }

	  if (run_flag)
	  {
		  if (current_duty < default_duty && spd_inc_flag)
		  {
			  spd_inc_flag = false;
			  current_duty += 5;
		  }
		  htim1.Instance->CCR1 = current_duty;
	      lcd_wr_stct.xpos = 65;
		  lcd_wr_stct.ypos = 35;
		  sprintf(lcd_wr_stct.line, "%d'", tim14_counter);

		  if (tim14_counter <= 0)
		  {
			  run_status_deinit();
		      MX_RTC_Init();
		  }
	  }

	  HAL_RTC_GetTime(&hrtc, &stimestructureget, RTC_FORMAT_BIN);
	  HAL_RTC_GetDate(&hrtc, &sdatestructureget, RTC_FORMAT_BIN);

	  if (stimestructureget.Seconds >= 3 && !run_flag)
	  {
		  LcdSetBrightness(LOW_BRIGHTNESS);
	  }

	  if (stimestructureget.Seconds >= 10 && !run_flag)
	  {
		  LcdClearScreen();
		  LcdSetBrightness(ZERO_BRIGHTNESS);
	  }

	  if (stimestructureget.Seconds >= 15 && !run_flag)
	  {
		  //enter_stop_mode();
		  __NOP();
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

